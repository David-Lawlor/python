# operators are the same as other programming languages

# addition
print(5 + 5)

# subraction
print(5 - 5)

# multiplication
print(5 * 5)

# division
print(5 / 5)

# result of 5/3 is rounded up by the round function
print(round(5/3))

# result is rounded down using //
print(5//3)

# modulus(remainder
print((5%3))

# power
print((5**2))